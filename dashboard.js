// Format date helper
function formatDate(dateString) {
    const date = new Date(dateString);
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    return date.toLocaleDateString('en-US', options);
}

// Update last updated timestamp
function updateLastUpdated() {
    const lastUpdated = document.getElementById('last-updated');
    if (lastUpdated) lastUpdated.textContent = new Date().toLocaleString();
}

// Theme Logic (No localStorage, System Default)
function initTheme() {
    // Check system preference
    if (window.matchMedia && window.matchMedia('(prefers-color-scheme: dark)').matches) {
        document.documentElement.classList.add('dark');
    }
}

function toggleTheme() {
    document.documentElement.classList.toggle('dark');
}

// Initialize dashboard
async function initDashboard() {
    try {
        initTheme(); // Initialize theme

        // Theme toggle listener
        const themeBtn = document.getElementById('theme-toggle');
        if (themeBtn) {
            themeBtn.addEventListener('click', toggleTheme);
        }

        // Load all data
        const config = await loadConfig();

        // Render all sections
        renderHeader(config);
        renderProjectInfo(config);
        const githubData = await loadGitHubData(config);
        renderGitHubStats(githubData);
        renderSlackInfo(config);
        const blogPosts = await loadBlogPosts();
        renderBlogPosts(blogPosts);
        const mentorData = await loadMentorInteractions();
        renderMentorInfo(mentorData);
        const weeklyUpdates = await loadWeeklyUpdates();
        renderWeeklyUpdates(weeklyUpdates);
        const milestones = await loadMilestones();
        renderMilestones(milestones);
        updateLastUpdated();
    } catch (error) {
        console.error('Error initializing dashboard:', error);
    }
}

// Initialize when DOM is loaded
