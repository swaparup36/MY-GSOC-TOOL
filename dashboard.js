// Render mentor info
function renderMentorInfo(mentorData) {
    if (!mentorData) {
        console.warn('renderMentorInfo: missing mentorData');
        return;
    }

    const mentorDetails = document.getElementById('mentor-details');
    const feedbackList = document.getElementById('feedback-list');

    if (!mentorDetails || !feedbackList) {
        console.warn('renderMentorInfo: required DOM elements missing');
        return;
    }

    // data/mentor-interactions.json now contains { mentors: [...], interactions: [...] }
    const mentors = mentorData.mentors || [];
    const interactions = mentorData.interactions || [];

    // Removed unused leadMentorTitle and coMentorTitle variables

    if (mentors.length === 0) {
        mentorDetails.innerHTML =
            '<p class="text-gray-500 italic">Mentor information not configured in data/mentor-interactions.json.</p>';
    } else {
        mentorDetails.innerHTML = mentors
            .map((mentor) => {
                return `
                <div class="flex items-center gap-4 p-4 border border-gray-100 dark:border-gray-700 rounded-lg bg-white dark:bg-gray-700/50">
                    <img src="${mentor.avatar || 'assets/images/sample-mentor.svg'}"
                         alt="${mentor.name}"
                         class="w-12 h-12 rounded-full object-cover border-2 border-gray-200 dark:border-gray-600">
                    <div class="flex-1 min-w-0">
                        <h3 class="font-medium text-gray-900 dark:text-white truncate">${mentor.name}</h3>
                        <p class="text-sm text-gray-500 dark:text-gray-400">${mentor.role || 'Mentor'}</p>
                        ${mentor.email ? `<p class="text-xs text-gray-400 dark:text-gray-500 mt-1 flex items-center"><i class="fas fa-envelope mr-1"></i> ${mentor.email}</p>` : ''}
                        ${mentor.github ? `<p class="text-xs text-primary mt-1"><a href="${mentor.github}" target="_blank" class="hover:underline flex items-center"><i class="fab fa-github mr-1"></i> GitHub</a></p>` : ''}
                    </div>
                </div>
                `;
            })
            .join('');
    }

    if (interactions.length > 0) {
        feedbackList.innerHTML = interactions
            .map((item) => {
                const dateText = item.date ? formatDate(item.date) : '';
                return `
                <div class="p-4 bg-gray-50 dark:bg-gray-700/30 rounded-lg border border-gray-100 dark:border-gray-700">
                    <div class="flex justify-between items-center mb-2">
                        <strong class="text-gray-800 dark:text-gray-200 font-medium">${item.from}</strong>
                        <span class="text-xs text-gray-500 dark:text-gray-500">${dateText}</span>
                    </div>
                    <div class="text-gray-600 dark:text-gray-400 text-sm leading-relaxed">${item.content || ''}</div>
                </div>
                `;
            })
            .join('');
    } else {
        feedbackList.innerHTML =
            '<p class="text-gray-500 italic">No interactions logged yet. Add them to data/mentor-interactions.json</p>';
    }
}

