// Blog Modal Logic
function openBlogModal(index) {
    const post = window.blogPostsData[index];
    if (!post) return;

    const modal = document.getElementById('blog-modal');
    const title = document.getElementById('blog-modal-title');
    const date = document.getElementById('blog-modal-date');
    const content = document.getElementById('blog-modal-content');

    title.textContent = post.title;
    date.innerHTML = `<i class="fas fa-calendar mr-1"></i> ${formatDate(post.date)}`;
    content.innerHTML = post.content || `<p>${post.excerpt}</p><p class="italic text-gray-500 mt-4">(No full content available)</p>`;

    modal.classList.remove('hidden');
    // Prevent body scroll
    document.body.style.overflow = 'hidden';
}

function closeBlogModal() {
    const modal = document.getElementById('blog-modal');
    modal.classList.add('hidden');
    // Restore body scroll
    document.body.style.overflow = 'auto';
}

// Event listeners for modal
document.addEventListener('DOMContentLoaded', () => {
    const modal = document.getElementById('blog-modal');
    const closeBtn = document.getElementById('blog-modal-close');

    if (closeBtn) {
        closeBtn.addEventListener('click', closeBlogModal);
    }

    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeBlogModal();
            }
        });
    }

    // Close on Escape key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && !modal.classList.contains('hidden')) {
            closeBlogModal();
        }
    });
});

