// Helper to extract owner and repo from URL
function getRepoDetails(url) {
    if (!url) return null;
    try {
        const urlObj = new URL(url);
        const path = urlObj.pathname.replace(/^\/|\/$/g, '');
        const parts = path.split('/');
        if (parts.length >= 2) {
            return { owner: parts[0], repo: parts[1] };
        }
    } catch (e) {
        console.error("Invalid repo URL", e);
    }
    return null;
}

// Helper to extract username from URL
function getUsername(url) {
    if (!url) return null;
    try {
        const urlObj = new URL(url);
        const path = urlObj.pathname.replace(/^\/|\/$/g, '');
        const parts = path.split('/');
        if (parts.length >= 1) {
            return parts[0];
        }
    } catch (e) {
        console.error("Invalid github URL", e);
    }
    return null;
}

// Fetch GitHub data dynamically (supports multiple repos)
async function loadGitHubData(config) {
    // Try to fetch static data first (generated by CI/CD)
    try {
        const response = await fetch('data/github-contributions.json');
        if (response.ok) {
            const data = await response.json();
            // Check if data is not stale (optional, e.g. > 24h old)
            // For now, just return it
            return data;
        }
    } catch (e) {
        console.log('Static GitHub data not found, falling back to API');
    }

    const username = getUsername(config.student.github);

    // Normalize repositories list: handle array or single string (legacy)
    let repositories = [];
    if (config.project.repositories && Array.isArray(config.project.repositories)) {
        repositories = config.project.repositories;
    } else if (config.project.repository) {
        repositories = [config.project.repository];
    }

    if (repositories.length === 0 || !username) {
        console.warn("Repositories list or student GitHub URL missing/invalid in config.");
        return {
            stats: { commits: 0, pullRequests: 0, issues: 0, reviews: 0 },
            contributions: []
        };
    }

    const sixMonthsAgo = new Date();
    sixMonthsAgo.setMonth(sixMonthsAgo.getMonth() - 6);
    const dateStr = sixMonthsAgo.toISOString().split('T')[0]; // YYYY-MM-DD

    let totalStats = { commits: 0, pullRequests: 0, issues: 0, reviews: 0 };
    let allContributions = [];

    // Process all repositories in parallel
    const promises = repositories.map(async (repoUrl) => {
        const repoDetails = getRepoDetails(repoUrl);
        if (!repoDetails) return;

        const { owner, repo } = repoDetails;

        try {
            // Fetch Pull Requests (Only merged)
            const prQuery = `repo:${owner}/${repo} is:pr is:merged author:${username} created:>${dateStr}`;
            const prResponse = await fetch(`https://api.github.com/search/issues?q=${encodeURIComponent(prQuery)}`);
            const prData = await prResponse.json();
            const prCount = prData.total_count || 0;

            // Fetch Issues
            const issueQuery = `repo:${owner}/${repo} is:issue author:${username} created:>${dateStr}`;
            const issueResponse = await fetch(`https://api.github.com/search/issues?q=${encodeURIComponent(issueQuery)}`);
            const issueData = await issueResponse.json();
            const issueCount = issueData.total_count || 0;

            // Fetch Reviews (Exclude own PRs)
            const reviewQuery = `repo:${owner}/${repo} is:pr reviewed-by:${username} -author:${username} created:>${dateStr}`;
            const reviewResponse = await fetch(`https://api.github.com/search/issues?q=${encodeURIComponent(reviewQuery)}`);
            const reviewData = await reviewResponse.json();
            const reviewCount = reviewData.total_count || 0;

            // Fetch Commits - Use List API per repo (more reliable than search for stats)
            let commitCount = 0;
            const commitResponse = await fetch(`https://api.github.com/repos/${owner}/${repo}/commits?author=${username}&since=${sixMonthsAgo.toISOString()}&per_page=1`);

            const linkHeader = commitResponse.headers.get('link');
            if (linkHeader) {
                const match = linkHeader.match(/&page=(\d+)>; rel="last"/);
                if (match) {
                    commitCount = parseInt(match[1], 10);
                } else {
                    // Single page result
                    const data = await commitResponse.json();
                    commitCount = Array.isArray(data) ? data.length : 0;
                }
            } else {
                if (commitResponse.ok) {
                    const data = await commitResponse.json();
                    if (Array.isArray(data)) commitCount = data.length;
                }
            }

            // Collect contributions
            if (prData.items && prData.items.length > 0) {
                const recentPRs = prData.items.slice(0, 5).map(item => ({
                    title: item.title,
                    description: `Pull Request #${item.number} in ${owner}/${repo}`,
                    date: item.created_at,
                    url: item.html_url,
                    timestamp: new Date(item.created_at).getTime()
                }));
                allContributions.push(...recentPRs);
            }

            // Update totals (safely)
            totalStats.commits += commitCount;
            totalStats.pullRequests += prCount;
            totalStats.issues += issueCount;
            totalStats.reviews += reviewCount;

        } catch (error) {
            console.error(`Error fetching data for ${owner}/${repo}:`, error);
        }
    });

    await Promise.all(promises);

    // Sort contributions by date desc and take top 5
    const sortedContributions = allContributions.sort((a, b) => b.timestamp - a.timestamp).slice(0, 5);
    // Remove temporary timestamp
    // const finalContributions = sortedContributions.map(({timestamp, ...rest}) => rest);

    return {
        stats: totalStats,
        contributions: sortedContributions
    };
}

